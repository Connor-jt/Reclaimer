namespace Reclaimer.Geometry
{
    public static class StandardUnits
    {
        public const float Default = 1;

        public const float Millimeters = Default;
        public const float Meters = Millimeters * 1000;

        public const float Inches = Millimeters * 25.4f;
        public const float Feet = Inches * 12;
    }
}
